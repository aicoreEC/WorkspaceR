# 2.2 상관분석
#
# - 두 변수간의 관계를 분석시 추세의 모양이 선의 형태를 갖는 것을
#   선형적 관계라 표현한다.
#
# - 선형적 관계는 강한 선형적 관계가 있고 약한 선형적 관계가 있다.
#
# - 상관분석( correlation analysis ) : 얼마나 선형성을 보이는지 수칙상으로
#                                      나타내는 방법
#
# - 상관계수( correlation coefficient )는 선형성의 정도를 나타내는 척도
# - 상관계수 r의 성질
#   -1 <= r <= 1
#   r > 0 : 양의 상관계수( x가 증가하면 y도 증가 )
#   r < 0 : 음의 상관계수( x가 증가하면 y는 감소 )
#   r이 1이나 -1에 가까울수록 x, y의 상관성이 높다.
#
# - 상관계수값이 1이나 -1에 가까울수록 관측값들의 분포가 직선에 가까워지다
#


